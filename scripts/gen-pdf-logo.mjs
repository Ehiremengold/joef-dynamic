/**
 * Regenerate lib/pdf/logo-base64.ts from public/images/logo.png.
 * Run this whenever the school crest image changes:
 *   node scripts/gen-pdf-logo.mjs
 *
 * The logo is embedded as a base64 string directly in the JS bundle (rather
 * than read from disk at request time) so PDF generation never depends on
 * public/ being present in the serverless function's filesystem at runtime.
 */
import { readFileSync, writeFileSync } from "node:fs";

const png = readFileSync(new URL("../public/images/logo.png", import.meta.url));
const base64 = png.toString("base64");

const out = `// Auto-generated by scripts/gen-pdf-logo.mjs — do not edit by hand.
export const LOGO_PNG_BASE64 = ${JSON.stringify(base64)};
`;

writeFileSync(new URL("../lib/pdf/logo-base64.ts", import.meta.url), out);
console.log(`Wrote lib/pdf/logo-base64.ts (${(base64.length / 1024).toFixed(0)} KB base64)`);
